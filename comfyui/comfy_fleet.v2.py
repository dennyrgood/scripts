def main():
    parser = argparse.ArgumentParser(description="ComfyUI Fleet Analysis")
    parser.add_argument("--config", default=None, help="Path to fleet_config.json")
    parser.add_argument("--year",   default=None, help="Override workflow year filter")
    args = parser.parse_args()

    # Find config - check: explicit arg, CWD, then script directory
    if args.config:
        config_path = Path(args.config)
    else:
        cwd_config    = Path.cwd() / "fleet_config.json"
        script_config = Path(__file__).parent / "fleet_config.json"
        if cwd_config.exists():
            config_path = cwd_config
        elif script_config.exists():
            config_path = script_config
        else:
            print("ERROR: fleet_config.json not found.")
            print(f"  Looked in: {Path.cwd()} and {Path(__file__).parent}")
            print("  Pass --config /path/to/fleet_config.json or put fleet_config.json in current directory.")
            sys.exit(1)

    print(f"Loading config: {config_path}")
    config      = load_config(config_path)
    year_filter = args.year or config.get("workflow_year_filter", "2026")
    reports_dir = Path(config["reports_dir"])
    output_dir  = Path(config.get("output_dir", reports_dir / "fleet-output"))
    history_dir = Path(config.get("history_dir", reports_dir / "history"))
    timestamp   = datetime.now().strftime("%Y-%m-%d_%H%M")

    output_dir.mkdir(parents=True, exist_ok=True)
    history_dir.mkdir(parents=True, exist_ok=True)

    print(f"\nReports dir : {reports_dir}")
    print(f"Output dir  : {output_dir}")
    print(f"Year filter : {year_filter}")
    print()

    # Load all machine data
    data = load_all_machines(config)
    if not data:
        print("ERROR: No machine data loaded. Check reports_dir and file patterns.")
        sys.exit(1)

    # Load history for drift detection
    previous = load_history(history_dir)
    drift    = compute_drift(previous, data)
    if previous:
        print(f"\nDrift comparison vs: {previous.get('timestamp','?')}")
    else:
        print("\nNo previous snapshot found — this run will be the baseline.")

    # Build universe
    universe = build_model_universe(data)
    wf_data  = build_workflow_model_data(data, year_filter)

    # Identify source machine
    source_host = next(
        (h for h, m in config["machines"].items() if m.get("is_source")),
        list(data.keys())[0]
    )
    src_models_root = config["sync"]["source_models_root"]
    dest_models_bare = config["sync"]["dest_models_bare"]

    # Readiness per machine
    readiness = {}
    for hostname in data:
        readiness[hostname] = compute_readiness(data, wf_data, hostname, year_filter)

    # Node analysis
    nodes_data = analyze_nodes(data, wf_data)

    # Model gaps
    vram_ok    = config["vram_thresholds"]["ok"]
    vram_maybe = config["vram_thresholds"]["maybe"]

    source_models = data[source_host]["models"] if source_host in data else {}

    # Group non-source machines by sync_group (explicit) or fall back to hostname
    # Machines in the same sync_group share a Models_bare destination
    gaps         = []
    sync_scripts = {}

    dest_groups = defaultdict(list)
    for hostname, machine in data.items():
        if hostname == source_host:
            continue
        group_key = config["machines"][hostname].get("sync_group", hostname)
        dest_groups[group_key].append(hostname)

    for dest_bare, hostnames_in_group in dest_groups.items():
        # Use the lowest VRAM in the group as the constraint
        vram = min(config["machines"][h].get("vram_gb", 99) for h in hostnames_in_group)
        group_label = " + ".join(hostnames_in_group)

        # Union of what ALL machines in this group are missing
        # (if they share Models_bare, missing set is identical, but union is safe)
        missing_keys = set()
        for hostname in hostnames_in_group:
            machine = data[hostname]
            for fn_lower in wf_data:
                if fn_lower not in machine["models"] and fn_lower in source_models:
                    if max_wf_count(wf_data[fn_lower]) >= 3:
                        missing_keys.add(fn_lower)

        missing = []
        for fn_lower in missing_keys:
            sz   = universe.get(fn_lower, {}).get("size_gb", 0)
            cat  = universe.get(fn_lower, {}).get("category", "")
            rel  = source_models[fn_lower].get("relative_path", fn_lower)
            missing.append({
                "filename":       source_models[fn_lower]["filename"],
                "filename_lower": fn_lower,
                "category":       cat,
                "size_gb":        sz,
                "wf_count":       max_wf_count(wf_data[fn_lower]),
                "relative_path":  rel,
            })
        missing.sort(key=lambda x: x["wf_count"], reverse=True)

        ok_models    = [m for m in missing if m["size_gb"] <= vram_ok]
        maybe_models = [m for m in missing if vram_ok < m["size_gb"] <= vram_maybe]
        big_models   = [m for m in missing if m["size_gb"] > vram_maybe]

        # Script name based on group (use first hostname if shared)
        script_tag = hostnames_in_group[0] if len(hostnames_in_group) == 1 else "ModelsBase"

        gaps.append({
            "title":       f"Copy to {group_label} — OK models (<= {vram_ok} GB, safe for {vram}GB VRAM)",
            "models":      ok_models,
            "script_name": f"sync_{script_tag}_MIN.bat",
            "is_sync":     True,
            "priority":    "high",
        })
        gaps.append({
            "title":       f"Copy to {group_label} — MAYBE models ({vram_ok}-{vram_maybe} GB, marginal for {vram}GB VRAM)",
            "models":      maybe_models,
            "script_name": f"sync_{script_tag}_MAYBE.bat",
            "is_sync":     True,
            "priority":    "medium",
        })
        gaps.append({
            "title":       f"Skip for {group_label} — BIG models (> {vram_maybe} GB, too large for {vram}GB VRAM)",
            "models":      big_models,
            "is_sync":     False,
            "priority":    "low",
        })

        if ok_models:
            script = generate_robocopy(
                ok_models,
                f"sync_{script_tag}_MIN  -- Safe for {vram}GB VRAM (<= {vram_ok}GB each)",
                f"Copies to Models_bare shared by: {group_label}",
                src_models_root, dest_models_bare
            )
            sync_scripts[f"sync_{script_tag}_MIN.bat"] = script

        if maybe_models:
            script = generate_robocopy(
                maybe_models,
                f"sync_{script_tag}_MAYBE  -- Marginal ({vram_ok}-{vram_maybe}GB each)",
                f"Run after MIN. Marginal for {vram}GB VRAM — test before travelling.",
                src_models_root, dest_models_bare
            )
            sync_scripts[f"sync_{script_tag}_MAYBE.bat"] = script

    # Subdir mismatch analysis
    subdir_mismatches = analyze_subdir_mismatches(data, config)

    # Build actions
    report_data = {
        "machines":         data,
        "drift":            drift,
        "readiness":        readiness,
        "nodes":            nodes_data,
        "gaps":             gaps,
        "universe":         universe,
        "actions":          [],
        "subdir_mismatches": subdir_mismatches,
    }
    report_data["actions"] = build_actions(gaps, readiness, drift, nodes_data)

    # --- Write outputs ---

    # Robocopy scripts
    for name, script in sync_scripts.items():
        out = output_dir / name
        out.write_text(script)
        print(f"Written: {out.name}")

    # Custom nodes text report
    nodes_lines = []
    nodes_lines.append("CUSTOM NODES FLEET COMPARISON")
    nodes_lines.append(f"Generated: {timestamp}")
    nodes_lines.append("=" * 68)
    nodes_lines.append(f"  {'Node':<45} " + "  ".join(f"[{h[:6]}]" for h in nodes_data["hostnames"]))
    nodes_lines.append("-" * 68)
    for node in nodes_data["all_nodes"]:
        row = nodes_data["matrix"][node]
        cols = "     ".join("Y" if row.get(h) else " " for h in nodes_data["hostnames"])
        nodes_lines.append(f"  {node:<45} {cols}")
    nodes_lines.append("")
    nodes_lines.append("ON ALL MACHINES: " + ", ".join(nodes_data["on_all"]))
    (output_dir / f"custom_nodes_{timestamp}.txt").write_text("\n".join(nodes_lines))
    print(f"Written: custom_nodes_{timestamp}.txt")

    # Subdir mismatch report
    if subdir_mismatches:
        mismatch_lines = [
            "SUBDIR MISMATCH REPORT",
            f"Generated: {timestamp}",
            "Models that exist on both ImageBeast and Models_bare but in different subdirectories.",
            "Fix: run fix_subdir_structure.bat to copy to correct location, then delete old.",
            "=" * 72,
            "",
        ]
        for m in subdir_mismatches:
            mismatch_lines.append(f"  {m['filename']}  ({m['size_gb']:.2f} GB)")
            mismatch_lines.append(f"    ImageBeast (correct): {m['src_rel']}")
            mismatch_lines.append(f"    Models_bare (current): {m['dest_rel']}")
            mismatch_lines.append("")
        mismatch_lines.append(f"Total: {len(subdir_mismatches)} mismatches")
        (output_dir / f"subdir_mismatches_{timestamp}.txt").write_text("\n".join(mismatch_lines))
        print(f"Written: subdir_mismatches_{timestamp}.txt")

        fix_bat = generate_subdir_fix_bat(subdir_mismatches, src_models_root, dest_models_bare)
        (output_dir / "fix_subdir_structure.bat").write_text(fix_bat)
        print(f"Written: fix_subdir_structure.bat  ({len(subdir_mismatches)} files to relocate)")
    else:
        print("Subdir structure: OK (no mismatches found)")

    # HTML report
    html = generate_html(report_data, timestamp, year_filter)
    html_path = output_dir / f"fleet_report_{timestamp}.html"
    html_path.write_text(html)
    print(f"Written: fleet_report_{timestamp}.html")

    # Text summary
    summary_lines = []
    summary_lines.append(f"ComfyUI Fleet Analysis  {timestamp}")
    summary_lines.append(f"Year filter: {year_filter}")
    summary_lines.append("=" * 60)
    summary_lines.append("")
    summary_lines.append("READINESS")
    summary_lines.append("-" * 60)
    for h, r in readiness.items():
        vram = config["machines"][h].get("vram_gb", "?")
        summary_lines.append(f"  {h:<16} {r.get('pct',0):>3}%  ({r.get('can_run',0)}/{r.get('total',0)} workflows)  {vram}GB VRAM")
    summary_lines.append("")
    summary_lines.append("NEXT ACTIONS")
    summary_lines.append("-" * 60)
    for a in report_data["actions"]:
        p = {"high": "!!!", "medium": " ! ", "low": "   "}.get(a["priority"], "   ")
        summary_lines.append(f"  [{p}]  {a['title']}")
        summary_lines.append(f"         {a['detail']}")
    (output_dir / f"summary_{timestamp}.txt").write_text("\n".join(summary_lines))
    print(f"Written: summary_{timestamp}.txt")

    # Save snapshot for next run's drift detection
    save_snapshot(history_dir, data, timestamp)

    # Print summary to console
    print()
    print("\n".join(summary_lines))
    print(f"\nAll outputs in: {output_dir}")


if __name__ == "__main__":
    main()
