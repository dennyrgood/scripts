# Next actions builder
# ---------------------------------------------------------------------------

def build_actions(gaps: list, readiness: dict, drift: dict, nodes_data: dict) -> list:
    actions = []

    # Model sync actions - use gap's own priority field
    seen_scripts = set()
    for gap in gaps:
        if gap.get("is_sync") and gap["models"]:
            script = gap.get("script_name", "")
            if script in seen_scripts:
                continue
            seen_scripts.add(script)
            total_gb = sum(m["size_gb"] for m in gap["models"])
            priority = gap.get("priority", "medium")
            actions.append({
                "priority": priority,
                "title":    f"Run {script} on ImageBeast",
                "detail":   f"{len(gap['models'])} files, {total_gb:.2f} GB — copies to Models_bare for Travel + Chat",
            })

    # Readiness - only flag if low AND not explained by VRAM
    for h, r in readiness.items():
        pct = r.get("pct", 100)
        if pct < 60:
            actions.append({
                "priority": "medium",
                "title":    f"{h} readiness {pct}% — expected for lower-VRAM machine",
                "detail":   f"{r['can_run']}/{r['total']} workflows runnable. Blocked workflows require large models only suited for ImageBeast.",
            })

    # Missing nodes on TravelBeast
    travel_missing_nodes = nodes_data["missing"].get("TRAVELBEAST", [])
    high_value = [n for n in travel_missing_nodes if n in {
        "comfyui-qwenvl", "comfyui_ipadapter_plus", "was-ns",
        "vantage-dreamomni2", "comfyui_layerstyle", "comfyui-advancedliveportrait",
    }]
    if high_value:
        actions.append({
            "priority": "medium",
            "title":    "Install missing nodes on TravelBeast via ComfyUI Manager",
            "detail":   "Priority: " + ", ".join(high_value[:5]),
        })

    # Drift
    for h, d in drift.items():
        removed = d.get("models_removed", [])
        if removed:
            actions.append({
                "priority": "medium",
                "title":    f"{h}: {len(removed)} models removed since last run",
                "detail":   "Check: " + ", ".join(removed[:3]),
            })

    # Unused on ImageBeast
    actions.append({
        "priority": "low",
        "title":    "Review unused models on ImageBeast",
        "detail":   "Run cleanup to reclaim disk space (see Unused Models section)",
    })

    return actions


