def load_history(history_dir: Path) -> dict | None:
    history_dir.mkdir(parents=True, exist_ok=True)
    files = sorted(history_dir.glob("snapshot-*.json"))
    if not files:
        return None
    with open(files[-1]) as f:
        return json.load(f)


def save_snapshot(history_dir: Path, data: dict, timestamp: str):
    history_dir.mkdir(parents=True, exist_ok=True)
    snapshot = {
        "timestamp": timestamp,
        "machines": {}
    }
    for hostname, machine in data.items():
        snapshot["machines"][hostname] = {
            "model_count": len(machine["models"]),
            "node_count":  len(machine["nodes"]),
            "models":      sorted(machine["models"].keys()),
            "nodes":       sorted(machine["nodes"]),
        }
    out = history_dir / f"snapshot-{timestamp}.json"
    with open(out, "w") as f:
        json.dump(snapshot, f, indent=2)
    print(f"Snapshot saved: {out.name}")


def compute_drift(previous: dict | None, data: dict) -> dict:
    drift = {}
    if not previous:
        return drift

    for hostname, machine in data.items():
        if hostname not in previous.get("machines", {}):
            drift[hostname] = {"new_machine": True}
            continue

        prev = previous["machines"][hostname]
        curr_models = set(machine["models"].keys())
        prev_models = set(prev.get("models", []))
        curr_nodes  = machine["nodes"]
        prev_nodes  = set(prev.get("nodes", []))

        drift[hostname] = {
            "models_added":   sorted(curr_models - prev_models),
            "models_removed": sorted(prev_models - curr_models),
            "nodes_added":    sorted(curr_nodes  - prev_nodes),
            "nodes_removed":  sorted(prev_nodes  - curr_nodes),
            "prev_date":      previous.get("timestamp", "unknown"),
        }

    return drift


