def load_all_machines(config: dict) -> dict:
    reports_dir = Path(config["reports_dir"])
    data = {}

    for hostname, machine_cfg in config["machines"].items():
        print(f"Loading {hostname}...")
        files = load_machine_files(reports_dir, hostname)
        if not files:
            print(f"  SKIP: no files found for {hostname}")
            continue

        entry = {
            "config":   machine_cfg,
            "files":    files,
            "models":   {},   # filename.lower() -> row
            "nodes":    set(),
            "full_map": [],
        }

        if "models" in files:
            rows = load_csv(files["models"])
            for r in rows:
                entry["models"][r["filename"].lower()] = r
            print(f"  {len(entry['models'])} models")

        if "nodes" in files:
            entry["nodes"] = load_nodes(files["nodes"])
            print(f"  {len(entry['nodes'])} custom nodes")

        if "full_map" in files:
            entry["full_map"] = load_csv(files["full_map"])
            print(f"  {len(entry['full_map'])} full_map rows")

        if "unused" in files:
            entry["unused"] = load_csv(files["unused"])

        if "missing" in files:
            entry["missing_csv"] = load_csv(files["missing"])

        data[hostname] = entry

    return data


