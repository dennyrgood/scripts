def generate_robocopy(models_to_copy: list, label: str, note: str,
                      src_root: str, dest_root: str) -> str:
    total_gb = sum(m["size_gb"] for m in models_to_copy)
    lines = [
        "@echo off",
        "REM " + "=" * 60,
        f"REM  {label}",
        f"REM  {note}",
        f"REM  {len(models_to_copy)} files  |  {total_gb:.2f} GB",
        "REM  Run this on IMAGEBEAST",
        f"REM  Source : {src_root}",
        f"REM  Dest   : {dest_root}",
        "REM  OneDrive syncs automatically to TravelBeast + ChatWorkhorse",
        "REM " + "=" * 60,
        "",
    ]

    by_subdir = defaultdict(list)
    for m in models_to_copy:
        rel   = m.get("relative_path", m["filename"])
        parts = rel.replace("\\\\", "\\").split("\\")
        subdir = "\\".join(parts[:-1])
        by_subdir[subdir].append(m)

    for subdir in sorted(by_subdir.keys()):
        entries  = by_subdir[subdir]
        src_dir  = f"{src_root}\\{subdir}" if subdir else src_root
        dst_dir  = f"{dest_root}\\{subdir}" if subdir else dest_root
        label_str = subdir if subdir else "(root)"
        lines.append(f"REM --- {label_str} ---")
        for m in sorted(entries, key=lambda x: x["filename"]):
            lines.append(f'REM   {m["size_gb"]:.2f} GB  [{m.get("wf_count",0)} wf]  {m["filename"]}')
        file_args = " ".join(f'"{m["filename"]}"' for m in entries)
        lines.append(f'robocopy "{src_dir}" "{dst_dir}" {file_args} /COPY:DAT /R:2 /W:5')
        lines.append("")

    lines += [
        "echo.",
        "echo " + "=" * 60,
        f"echo  {label}  -- COMPLETE",
        f"echo  {len(models_to_copy)} files  ({total_gb:.2f} GB)",
        "echo  OneDrive will sync to TravelBeast and ChatWorkhorse",
        "echo " + "=" * 60,
        "pause",
    ]
    return "\n".join(lines)


