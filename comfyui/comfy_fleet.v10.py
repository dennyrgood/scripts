def find_latest(reports_dir: Path, hostname: str, pattern: str) -> Path | None:
    """Find the most recent file matching hostname + pattern glob."""
    matches = sorted(reports_dir.glob(f"{hostname}-{pattern}"))
    return matches[-1] if matches else None


def load_machine_files(reports_dir: Path, hostname: str) -> dict:
    """Load all report files for a machine, returning the latest of each type."""
    files = {}
    patterns = {
        "models":   "Models-*.csv",
        "full_map": "WorkflowMap-*-full_map.csv",
        "usage":    "WorkflowMap-*-model_usage.csv",
        "unused":   "WorkflowMap-*-unused_models.csv",
        "missing":  "WorkflowMap-*-missing_models.csv",
        "nodes":    "CustomNodes-*.txt",
    }
    for key, pattern in patterns.items():
        f = find_latest(reports_dir, hostname, pattern)
        if f:
            files[key] = f
        else:
            print(f"  WARNING: No {key} file found for {hostname}")
    return files


def load_csv(path: Path) -> list[dict]:
    with open(path, encoding="utf-8-sig") as f:
        return list(csv.DictReader(f))


def load_nodes(path: Path) -> set[str]:
    nodes = set()
    with open(path) as f:
        for line in f:
            line = line.strip()
            if line and line[0].isdigit():
                name = line.split(". ", 1)[-1].strip().lower()
                if name not in (".disabled", "__pycache__"):
                    nodes.add(name)
    return nodes


