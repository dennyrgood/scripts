def build_model_universe(data: dict) -> dict:
    """Build unified model info from all machines."""
    universe = {}
    for hostname, machine in data.items():
        for fn_lower, row in machine["models"].items():
            if fn_lower not in universe:
                universe[fn_lower] = {
                    "filename": row["filename"],
                    "category": row.get("category", ""),
                    "size_gb":  float(row.get("size_gb", 0)),
                    "on":       set(),
                    "paths":    {},
                }
            universe[fn_lower]["on"].add(hostname)
            universe[fn_lower]["paths"][hostname] = row.get("full_path", "")
    return universe


def build_workflow_model_data(data: dict, year_filter: str) -> dict:
    """
    For each model, collect which workflows reference it (filtered by year)
    and whether it's on disk on each machine.
    """
    model_wf = defaultdict(lambda: {
        "workflows": defaultdict(set),  # machine -> set of workflow names
        "on":        set(),
        "display":   "",
        "ref":       "",
    })

    for hostname, machine in data.items():
        for row in machine["full_map"]:
            if not row.get("workflow_modified", "").startswith(year_filter):
                continue
            fn   = row["model_filename"]
            ref  = row["model_ref"]
            ref_base = ref.replace("\\", "/").split("/")[-1]
            key  = fn.lower() if fn != "(not found on disk)" else ref_base.lower()

            wf_short = row["workflow_file"].split("\\")[-1]
            model_wf[key]["workflows"][hostname].add(wf_short)

            if fn != "(not found on disk)":
                model_wf[key]["on"].add(hostname)
            if not model_wf[key]["display"]:
                model_wf[key]["display"] = fn if fn != "(not found on disk)" else ref_base
            if not model_wf[key]["ref"]:
                model_wf[key]["ref"] = ref

    # Also mark presence from models CSV
    for hostname, machine in data.items():
        for key in model_wf:
            if key in machine["models"]:
                model_wf[key]["on"].add(hostname)

    return dict(model_wf)


def max_wf_count(model_entry: dict) -> int:
    return max((len(wfs) for wfs in model_entry["workflows"].values()), default=0)


